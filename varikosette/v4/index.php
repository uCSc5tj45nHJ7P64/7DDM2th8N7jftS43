<div class="dialog">
<img alt="" class="img" src="doc2.jpg">
<p>
                              Luego pierden fuerza y ​​comienzan a torcerse.&nbsp;Primero, aparecen las arañas vasculares, luego los nódulos y, finalmente, las venas se ven a través de la piel.  
                        </p>
</div>
<div class="dialog">
<img alt="" class="img" src="doc1_new.jpg">
<p>
<strong>   ¿Cómo podemos luchar contra esto?  
                        </strong>
</p>
</div>
<div class="dialog">
<img alt="" class="img" src="doc2.jpg">
<p>
                              Se han desarrollado muchos tratamientos para las varices como la farmacoterapia, la cirugía, el tratamiento con láser, la compresión elástica y la escleroterapia.  
                        </p>
<p>
                              Sin embargo, la farmacoterapia y la escleroterapia (inyecciones de una solución especial) tienen una eficacia muy baja y un impacto negativo en el hígado, mientras que la cirugía y el tratamiento con láser son bastante traumáticos.&nbsp;Además, hay altas tasas de recaídas 3-4 años después del tratamiento, lo que significa que las varices reaparecen.  
                        </p>
</div>
<div class="dialog">
<img alt="" class="img" src="doc2.jpg">
<p>
