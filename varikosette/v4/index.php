<h5>
                       ¿Curar las varices en casa en un mes?&nbsp;¡Ahora es posible!  
                  </h5>
<p>
<b>
<em class="em">     El doctor Juan Rodríguez Mendoza, uno de los especialistas latinoamericanos más destacados, habló de ello durante el programa de televisión "Doctores" en el canal Doctors Care.  
                     </em>
</b>
</p>
<p>
<span class="b-brand-episode__date tv-date">
                          Programa de televisión "Doctores" en el canal Doctors Care.  
                        {digitalDate:-4days}
</span>
</p>
<div class="dialog">
<img alt="" class="img" src="doc1_new.jpg">
<p>
<strong>    Según las estadísticas, una de cada tres mujeres desarrolla varices después de los 40, y esta tasa aumenta constantemente.&nbsp;Sin embargo, resulta ser que se puede detener esta tendencia tan opresiva.&nbsp;Hoy, Juan Rodríguez Mendoza, flebólogo y doctor, hablará sobre este problema.  
                        </strong>
</p>
</div>
<div class="cont_img">
<img alt="" class="doctor_img" src="cnn-tv2.jpg">
<span>  Juan Rodríguez Mendoza  </span>
</div>
<div class="dialog">
<img alt="" class="img" src="doc1_new.jpg">
<p>
<strong>    Hola, doctor Juan.  
                        </strong>
</p>
</div>
<div class="dialog">
<img alt="" class="img" src="doc2.jpg">
<p>
                              Hola, Pedro.  
                        </p>
</div>
<div class="dialog">
<img alt="" class="img" src="doc1_new.jpg">
<p>
<strong>    Díganos, doctor, ¿cuál es la causa de las varices?&nbsp;Hay quienes dicen que es por los genes, otros que se debe a los tacones altos, otros relacionan esta enfermedad con el sobrepeso, etc.  
                        </strong>
</p>
</div>
<div class="dialog">
<img alt="" class="img" src="doc2.jpg">
<p>
                              Las cosas que ha enumerado no representan la causa de las   <a href="{link}">  varices</a>, sino los factores de riesgo.&nbsp;Puede verlos en la pantalla en orden descendente desde el más importante al menos importante.&nbsp;La causa de la aparición de las varices es el mal funcionamiento de las válvulas venosas.&nbsp;El hecho es que solamente aparecen en los vasos sanguíneos venosos, es decir, en los vasos en los que la sangre fluye de abajo hacia arriba.  
                        </p>
</div>
<div class="dialog">
<img alt="" class="img" src="doc1_new.jpg">
<p>
<strong>    ¿Contra la gravedad?  
                        </strong>
</p>
</div>
<div class="dialog">
<img alt="" class="img" src="doc2.jpg">
<p>
                              Exactamente, y ese es el punto clave.&nbsp;Esto ocurre de una manera muy sorprendente - la sangre venosa toma el material "residual" de la piel y nuestros músculos y pasa a través de las venas superficiales, llegando a las llamadas venas comunicantes (perforantes), llamadas así porque conectan las venas superficiales con las venas profundas.&nbsp;En las venas comunicantes hay válvulas que permiten la entrada de sangre desde las venas superficiales y la retienen.&nbsp;Si miramos la pantalla, podemos ver cómo funcionan estas válvulas.  
                        </p>
</div>
<center>
<img alt="" class="veins" src="38.png">
</center>
<div class="dialog">
<img alt="" class="img" src="doc1_new.jpg">
<p>
<strong>    ¿Eso significa que las válvulas empujan la sangre?  
                        </strong>
</p>
</div>
<div class="dialog">
<img alt="" class="img" src="doc2.jpg">
<p>
                              Son las contracciones del corazón y los músculos las que empujan la sangre, pero estos dos factores son "pulsátiles".&nbsp;Las válvulas evitan que la sangre vuelva a fluir hacia abajo por la gravedad, por lo que el corazón deja entrar la sangre, pero no la deja salir.  
                        </p>
</div>
<div class="dialog">
<img alt="" class="img" src="doc1_new.jpg">
<p>
<strong>    ¿Cuándo las válvulas comienzan a funcionar mal?  
                        </strong>
</p>
</div>
<div class="dialog">
<img alt="" class="img" src="doc2.jpg">
<p>
                              En caso de circunstancias adversas, las válvulas dentro de las venas se atrofian.&nbsp;Las válvulas deterioradas dentro de la vena causan un flujo sanguíneo venoso deficiente.&nbsp;La sangre comienza a estancarse en las venas produciendo un esfuerzo excesivo.&nbsp;La vena adquiere un diámetro mayor y las válvulas no pueden cubrir físicamente todo el espacio dentro de aquella, lo que provoca el denominado flujo sanguíneo inverso.  
                        </p>
<p>
                              La pérdida de sangre en esa área se intensifica y vuelve a las venas superficiales.&nbsp;La presión dentro de las venas safenas aumenta tanto que las paredes de los vasos sanguíneos ya no son capaces de soportarla, por lo que se tuercen y comienzan a expandirse como un globo, empeorando la situación.&nbsp;Puede verlo claramente en la pantalla.  
                        </p>
</div>
<center>
<img alt="" class="veins" src="40.png">
</center>
<div class="dialog">
<img alt="" class="img" src="doc2.jpg">
<p>
                              Las primeras venas comienzan a hincharse.  
                          
                        </p>
</div>
<div class="manual">
<img alt="" class="veins" src="58.png">
