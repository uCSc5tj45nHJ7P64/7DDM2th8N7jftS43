<h1>
                    ¡Me deshice de las varices por el bien de mis nietos!
                </h1>
<div class="fbdiv">
<a class="btn" href="{link}"> Me gusta </a> <a class="btn" href="{link}"> Compartir </a>
<span style="font-size: 11px; padding-top: 5px;">  A 595 personas les gusta esto. Sé el primero de tus amigos.  </span>
