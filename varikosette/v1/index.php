<div class="article">
<p>
<img alt="" class="alignleft" src="ph_1.jpg" width="75px">
</p>
<p>
<strong>  Cristina  </strong>
</p>
<p>
                        Hoy te diré finalmente cómo salvé mis pies del terrible sufrimiento, de  <strong>
                        las venas varicosas  </strong>  , y que me llevó a hacer esto.
                    </p>
<h2>
                        Cómo aparecieron las venas varicosas en mi vida
                    </h2>
<p>
<img alt="" class="alignright" src="ph_2.jpg" width="40%">
</p>
<p>
                        Hasta los 61 años empecé a sentir mi edad. Siempre estaba feliz por mi vida, en mi maravillosa casa, con un gran jardín. No necesitaba ayuda, porque mi salud me permitía
                        <strong>  hacerme cargo de  </strong>  las obligaciones de la casa  <strong>  por mi misma,  </strong>  especialmente porque vivía sola en la casa. Y puede que se pregunten, ¿por qué hace todo eso?  <span class="red-bg"><strong>  Mi mayor gratificación era cuando mi hija venía a verme con mis nietos.</strong></span>
                        Cuando vienen de la ciudad tan cansados, hambrientos, atormentados, me encanta ofrecerles la comida que hago, hecha de las verduras y frutas que crecen en mi jardín. Y, por supuesto, también les doy algo de comida para que lleven, porque hoy en día en las tiendas sólo se encuentran las frutas y verduras cultivadas con productos químicos. Mientras que los míos son limpios y sanos.
                    </p>
<p>
<img alt="" class="alignleft" src="ph_3.jpg" width="30%">
</p>
<p>
<span class="red-bg">  La verdad es que,   <span class="red-text">  a los 61 comencé a notar que las venas de mis pies eran cada vez más visibles.  </span>   Al principio, no tanto, pero cada mes que pasaba, no sólo eran visibles en la piel, sino   <strong>  que también se hinchaban  </strong>  , y empezaron a verse como mangueras.   <span class="red-text">  Además, mis pies empezaron a hincharse, y tuve un horrible dolor que no podía soportar.  </span></span>
</p>
<p>
<img alt="" class="alignright" src="ph_4.jpg" width="30%">
</p>
<h2>
                        Cómo las varices cambiaron mi vida
                    </h2>
<p>
                        ¿Necesito mencionar que ya no pude trabajar en mi jardín? Fue imposible, casi  <span class="red-text"><strong>  ya no podía ir  </strong>   al segundo piso de mi casa porque cada paso era una pesadilla.  </span>
<strong>  Derrotada  </strong>  por el dolor, me esforcé en cultivar pepinos, tomates y manzanas para mis nietos.
                    </p>
<p>
                        Mi hija me pidió que me mudara a la ciudad, que dejara de trabajar en el jardín, pero siempre quise tanto cuidar de mi familia, y lamentaba mucho haber tenido que abandonarlo todo, era una lástima dejar mi trabajo en el jardín.  <strong>  Me negué a hacerlo  </strong>  . Como resultado, una parte de la cosecha ya no creció, una parte se pudrió porque no tenía las fuerzas para hacerme cargo de ella.
                    </p>
<p>
<img alt="" class="alignleft" src="ph_5.jpg" width="30%">
</p>
<p>
                        Mi hija siguió visitándome con mis nietos, pero me pidió que me
                        <strong>  hospitalizara  </strong>  o incluso ir a descansar a  <strong>  casa  </strong>  , y que alguien cuidara de mí. ¡Que ofendida me sentí!  <span class="red-bg">  Quería sentirme de nuevo como antes, cuando mi hija no me veía como una   <strong>  necesitada e inútil</strong>  vieja.</span>
                        Realmente quería que alguien me necesitara,<strong>  y deshacerme  </strong>  del dolor insoportable y de la enfermedad que progresaba.
                    </p>
<h2>
                        Buscando por un tratamiento para las venas varicosas
                    </h2>
<p>
                        Puedes pensar que todo ese año, no traté de deshacerse de la varicosis, pero eso no es cierto. Busqué información en Internet, le pregunté a mis amigos, mis parientes. Incluso intenté remedios populares (hice ungüentos especiales, usé compresas), tomé píldoras, probé algunas cremas contra la varicosis, pero todo fue en vano,  <strong>  nada me ayudó  </strong>  . Ninguno de estos remedios pudo aliviar mi dolor, ni hicieron las venas varicosas más pequeñas. Solo tuve la opción de ir  <strong>  al doctor  </strong>  , pero tenía miedo, ya que creía que me hospitalizarían. Pero no tenía alternativas. Por el bien de mis nietos, fui al hospital.
                    </p>
<h2>
                        Lo que el doctor me dijo
                    </h2>
<p>
                        Me sorprendió que al ver mis terribles pies, el doctor dijera: "¡Esto se curará en una semana!" ¡¿Cómo?! ¡Si no pude deshacerme de esta cosa horrible durante tanto tiempo! Y me dio una receta que tenía el nombre de  <strong><a href="{link}">
                        Varikosette  </a></strong>  escrito, y el enlace al sitio donde podía ordenarlo. Me explicó que incluso en las farmacias podía encontrar productos falsos, pero el sitio del fabricante vende sólo el producto original y controlado.
                    </p>
<p>
                        Para hacerlo más corto,  <span class="red-bg">  me dijo que   <strong><a href="{link}">  Varikosette  </a></strong>   contenía sólo   <strong>  ingredientes naturales  </strong>  , por eso se absorbe muy fácilmente en la piel, a diferencia de otros productos que contienen sustancias químicas. Penetra en las venas y elimina la causa de la hinchazón, apretando las venas para que éstas puedan recuperar su condición natural. Además, es un   <strong>  producto  </strong>   seguro para el organismo, incluso para mi edad, gracias a su composición natural.  </span>
</p>
<p>
                        Por lo que, inmediatamente pedí esta crema,  <strong>  y sentí el efecto desde el primer uso: refresca suavemente la piel, eliminando las sensaciones de dolor y relajando los pies.  </strong>
                        Tuve que usarlo por las mañanas y las noches. Tomé intencionalmente una foto antes de usar la crema, para que pudiera evaluar su eficiencia.
                    </p>
<p>
<img alt="" class="aligncenter" src="ph_6.jpg">
</p>
<h2>
                        ¡El resultado
                    </h2>
<p>
<span class="red-bg">  es simplemente un milagro!   <span class="red-text">  ¡Después de 10 días de usar esta crema, el dolor se había ido completamente!  </span>   Ahora soy de nuevo activa, y mi jardín se ha recuperado completamente :)   </span>
</p>
<p>
                        Estoy feliz de poder ser una abuela y madre de nuevo,  <strong>  luciendo como una persona normal  </strong>  para ellos, y no una inútil.
                    </p>
<p>
                        Cuido otra vez de lo que más me gusta y disfruto pasando tiempo con mis seres queridos. Por cierto, ¿te has dado cuenta que  <strong>  las venas han desaparecido  </strong>  completamente? Ahora ya no asusto a mis nietos con mis pies.
                    </p>
<p>
<img alt="" class="aligncenter" src="ph_7.jpg">
</p>
<p style="text-align: right">
<strong><i>  ¡Mantente saludable!  <br>  Cristina  </i></strong>
